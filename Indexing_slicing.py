# creating list
Drinks_ch = ['Sprite', 'Grape Soda', 'Lemonade', 'DR Peper', 'Pepsi', 'Cola', 'Sparkling water']
# item address   0     ,     1     ,      2    ,    3      ,     4  ,   5   ,          6

print('\n', Drinks_ch)  # Print whole list

# Indexing List
print("\n", Drinks_ch[0])  # Print only First item in the list

print("\n", Drinks_ch[4])  # Print Only last item in the list

print("\n", Drinks_ch[6])  # Print Only last item in the list

# Slicing the List
print("\n", Drinks_ch[2:4])  # Print item 2 and 4 only [start: End]

print("\n", Drinks_ch[1:5])  # Print from 1 to 5

# slicing style to mention items from  yo 5
print("\n", Drinks_ch[:5])  # Print whole list until item 5
